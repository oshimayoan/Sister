    public void ConnectServer() {
        try {
            Registry reg = LocateRegistry.getRegistry("127.0.0.1", 1099);
            rmi = (RemoteInterface) reg.lookup("server");
            this.setTitle("Connected");
        } catch(Exception ex) {
            System.out.println(ex);
        }
    }

